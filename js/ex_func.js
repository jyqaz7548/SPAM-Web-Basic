// 계산기
//사칙연산
//매개변수에 두개의 값을 집어넣고
//깂을 반환

function plus(a, b) {
  return a + b;
}

function minus(a, b) {
  return a - b;
}

function multi(a, b) {
  return a * b;
}

function Divi(a, b) {
  return a / b;
}

console.log(plus(3, 3));
console.log(minus(3, 3));
console.log(multi(3, 3));
console.log(Divi(3, 3));
